<h1>地酒とは</h1>
<p>
    地酒とは特定の地域で造られる日本酒、その土地のお酒です。<br>
    全国で流通する大手メーカーの製品や、日本酒の名醸地である<br>
    「伏見(京都)」や「灘(兵庫)」以外の日本酒を指すことが多いです。<br><br>

    焼酎文化の深い九州・沖縄地方ですが、九州北部は冬場の気温が低く、日本酒造りにも適した風土。<br>
    特に福岡は酒米「山田錦」の有数な産地で、豊かな水源を利用した高品質な酒造りが行われています。<br>
    福岡と沖縄は淡麗辛口ですが、それ以外の県では甘口が主流です。<br><br>

    日本酒には、その香りや味などを表現する様々な言葉があります。<br>
    表現を知り、味わいながらいただくことで、日本酒をより楽しんでいただくことができます。<br>
</p>
<hr>
<h1>香り</h1>
<ul>
    <li>フルーティー</li>華やかで甘みのある香り<br><br>
    <li>華やか</li>甘い果実や花に例えられる香り<br><br>
    <li>爽やか</li>酸味の強いスッキリした印象の香り<br><br>
    <li>ふくよか</li>日本酒が本来持つ、米、穀物のような<br>旨味成分を感じさせる香り<br><br>
    <li>熟成感</li>じっくり熟成させた熟成古酒の香り<br>
</ul>
<hr>
<h1>味</h1>
<ul>
    <li>香りの高い</li>まるでワインのようなフルーティー、フローラルで華やかな味を表現します。<br>日本酒初心者や女性にも向いている人気のあるタイプです。<br><br>
    <li>軽快でなめらか</li>控え目な上立ち香と新鮮で軽快な含み香が特徴。<br>なめらかでみずみずしい味を表現した言葉で、主に生酒に代表されます。<br><br>
    <li>コクのある</li>落ち着いた、やや重厚なほど良い苦みを持った味を表現しています。<br>味が豊富でよく調和している充実感を表しています。<br><br>
    <li>熟成</li>長期熟成させた古酒に代表的に使用される表現で、<br>重厚で程よい苦みと後味の良さを感じさせます。<br><br>
    <li>すっきり</li>淡麗辛口の日本酒に表現されることが多く、<br>軽快ですっきりと飲みやすい味を表現しています。<br>料理の味を邪魔しない控え目な味わいがイメージできます。<br><br>
    <li>ふくよか</li>旨味を女性的に表現した誉め言葉です。<br>米の旨味が口の中でふっくらと膨らんでいく様子がイメージできます。<br><br>
    <li>さっぱり</li>淡い味のお酒は「さっぱり」と表現します。<br>引き締まった味で後味を引かないため、飲みやすいのが特徴です。<br>
</ul>
